--Applies NPC settings
npcManager.setNpcSettings(sampleNPCSettings)

npcManager.registerDefines(npcID, {NPC.UNHITTABLE})

--Custom local definitions below


--Register events
function sampleNPC.onInitAPI()
	npcManager.registerEvent(npcID, sampleNPC, "onTickEndNPC")
	--npcManager.registerEvent(npcID, sampleNPC, "onTickEndNPC")
	--npcManager.registerEvent(npcID, sampleNPC, "onDrawNPC")
	--registerEvent(sampleNPC, "onNPCKill")
end

function sampleNPC.onTickEndNPC(v)
	--Don't act during time freeze
	if Defines.levelFreeze then return end
	
	local data = v.data
	v.collisionGroup = "FrankProjectile"
    Misc.groupsCollide["FrankBoss"]["FrankProjectile"] = false
    Misc.groupsCollide["FrankProjectile"]["FrankProjectile"] = false
	Misc.groupsCollide["FrankProjectile"]["FrankBall"] = false
	Misc.groupsCollide["FrankProjectile"]["FrankEnemy"] = false
	--If despawned
	if v.despawnTimer <= 0 then
		--Reset our properties, if necessary
		data.initialized = false
		return
	end

	--Initialize
	if not data.initialized then
		--Initialize necessary data.
		data.initialized = true
		v.consecutive = 0
	end

	--Depending on the NPC, these checks must be handled differently
	if v.heldIndex ~= 0 --Negative when held by NPCs, positive when held by players
	or v.isProjectile   --Thrown
	or v.forcedState > 0--Various forced states
	then
		-- Handling of those special states. Most NPCs want to not execute their main code when held/coming out of a block/etc.
		-- If that applies to your NPC, simply return here.
		-- return
	end
	v.animationFrame = v.ai1
	data.speedX = data.speedX or 5
	v.speedX = data.speedX * v.direction
	if v:mem(0x120, FIELD_BOOL) then
		v:mem(0x120, FIELD_BOOL, false)
	end
	if (v.collidesBlockLeft and v.direction == -1) or (v.collidesBlockRight and v.direction == 1) then
		v.direction = -v.direction
		v.consecutive = v.consecutive + 1
		SFX.play(3)
		if v.consecutive >= sampleNPCSettings.recoilConsecutive then
			v:kill(9)
			Animation.spawn(10,v.x+v.width/2-16,v.y+v.height/2-16)
			SFX.play(4)
		end
	end
	if v.animationFrame >= 0 then
		-- animation controlling
		v.animationFrame = npcutils.getFrameByFramestyle(v, {
			frame = data.frame,
			frames = sampleNPCSettings.frames
		});
	end
end

--Gotta return the library table!
return sampleNPC